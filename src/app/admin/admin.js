$(document).ready(function () {
  $('#numEst').change(function () {
      if($(this).val()>0 && $(this).val()<7 ){
        $('.pintar').empty();
        for(var i=0;i<$(this).val();i++){
          $('.pintar').append('<table style="background-color: #cccccc">\n' +
            '  <tr>\n' +
            '    <td>\n' +
            '      <label>Doc. Estudiante</label>\n' +
            '    </td>\n' +
            '    <td>\n' +
            '      <input type="number" name="estudiantes[]"required>\n' +
            '    </td>\n' +
            '  </tr>\n' +
            '</table>');
        }
      }else{
        if($(this).val()==0){
          $('.pintar').empty();
          $('.pintar').append('<label>Debes rellenar el campo de cantidad de estudiante!</label>');
        }else {
          if($(this).val()<=0){
            $('.pintar').empty();
            $('.pintar').append('<label>La cantidad de estudiantes no puede ser negativa!</label>');
          }else {
            $('.pintar').empty();
            $('.pintar').append('<label>No puedes registrar ' + $(this).val() + ' estudiantes!<label>');
          }
        }
      }
  });
});
